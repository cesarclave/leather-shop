-- CREATE WAREHOUSES
USE ROLE SYSADMIN;
CREATE WAREHOUSE IF NOT EXISTS DBT_LEATHER_SHOP_DEV_WH WAREHOUSE_SIZE=XSMALL INITIALLY_SUSPENDED=TRUE AUTO_SUSPEND=60;
CREATE WAREHOUSE IF NOT EXISTS DBT_DBT_LEATHER_SHOP_PRD_WH WAREHOUSE_SIZE=XSMALL INITIALLY_SUSPENDED=TRUE AUTO_SUSPEND=60;

CREATE WAREHOUSE IF NOT EXISTS _ORCHESTRATION_DEV_WH WAREHOUSE_SIZE=XSMALL INITIALLY_SUSPENDED=TRUE AUTO_SUSPEND=60;
CREATE WAREHOUSE IF NOT EXISTS _ORCHESTRATION_PRD_WH WAREHOUSE_SIZE=XSMALL INITIALLY_SUSPENDED=TRUE AUTO_SUSPEND=60;

-------------------------------------------------------------------------------------------------------------------------------------------
---- CREATE ROLES:
USE ROLE USERADMIN;
CREATE ROLE IF NOT EXISTS DBT_LEATHER_SHOP_DEV_SERVICE_ACCOUNT_ROLE;
CREATE ROLE IF NOT EXISTS DBT_LEATHER_SHOP_PRD_SERVICE_ACCOUNT_ROLE;
--
CREATE ROLE IF NOT EXISTS DATA_ANALYST_ROLE;
CREATE ROLE IF NOT EXISTS DATA_ENGINEER_ROLE;
CREATE ROLE IF NOT EXISTS DATA_ARCHITECT_ROLE;

USE ROLE SYSADMIN;
GRANT ROLE DBT_LEATHER_SHOP_DEV_SERVICE_ACCOUNT_ROLE TO ROLE SYSADMIN;
GRANT ROLE DBT_LEATHER_SHOP_PRD_SERVICE_ACCOUNT_ROLE TO ROLE SYSADMIN;

GRANT ROLE DATA_ANALYST_ROLE TO ROLE SYSADMIN;
GRANT ROLE DATA_ENGINEER_ROLE TO ROLE SYSADMIN;
GRANT ROLE DATA_ARCHITECT_ROLE TO ROLE SYSADMIN;

-- ROLE HIERACHY
GRANT ROLE DATA_ANALYST_ROLE TO ROLE DATA_ENGINEER_ROLE;
GRANT ROLE DATA_ENGINEER_ROLE TO ROLE DATA_ARCHITECT_ROLE;
----------------------------------------------------------------------------------------------------------------------------------------
---- CREATE SERVICE ACCOUNT USERS:
USE ROLE USERADMIN;
CREATE USER IF NOT EXISTS DBT_LEATHER_SHOP_DEV_SERVICE_ACCOUNT_USER
    PASSWORD = 'your_dev_password'
    COMMENT= 'Service account for dbt in the development (DEV) environment of the CCLAVE project.'
    DEFAULT_WAREHOUSE = DBT_DBT_DBT_DBT_DBT_DBT_DBT_DBT_DBT_LEATHER_SHOP_DEV_WH
    DEFAULT_ROLE= DBT_LEATHER_SHOP_DEV_SERVICE_ACCOUNT_ROLE
    MUST_CHANGE_PASSWORD=FALSE;
    
CREATE USER IF NOT EXISTS DBT_LEATHER_SHOP_PRD_SERVICE_ACCOUNT_USER
    PASSWORD = 'your_prd_password'
    COMMENT= 'Service account for dbt in the production (PRD) environment of the CCLAVE project.'
    DEFAULT_WAREHOUSE = DBT_LEATHER_SHOP_PRD_WH
    DEFAULT_ROLE= DBT_LEATHER_SHOP_PRD_SERVICE_ACCOUNT_ROLE
    MUST_CHANGE_PASSWORD=FALSE;


