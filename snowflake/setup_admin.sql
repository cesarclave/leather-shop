---- CREATE OBJECTS
USE ROLE SYSADMIN;
---- DATABASES
CREATE DATABASE IF NOT EXISTS LEATHER_SHOP_DEV;
CREATE DATABASE IF NOT EXISTS LEATHER_SHOP_PRD;

CREATE DATABASE IF NOT EXISTS RAW_DEV;
CREATE DATABASE IF NOT EXISTS RAW_PRD;

-- SCHEMAS
CREATE SCHEMA IF NOT EXISTS RAW_DEV._ORCHESTRATION;
CREATE SCHEMA IF NOT EXISTS RAW_PRD._ORCHESTRATION;

CREATE SCHEMA IF NOT EXISTS RAW_DEV.RAW;
CREATE SCHEMA IF NOT EXISTS RAW_PRD.RAW;


-------------------------------------------------------------------------------------------------------------------------------------------
---- GRANT PRIVILEGES for DBT SERVICE ACCOUNTS:
USE ROLE SECURITYADMIN;
-----
-- FOR DBT_LEATHER_SHOP_DEV_SERVICE_ACCOUNT_ROLE
GRANT USAGE ON WAREHOUSE DBT_LEATHER_SHOP_DEV_WH TO ROLE DBT_LEATHER_SHOP_DEV_SERVICE_ACCOUNT_ROLE;
-- RAW DATA ACCESS
GRANT USAGE ON DATABASE LEATHER_SHOP_DEV TO ROLE DBT_LEATHER_SHOP_DEV_SERVICE_ACCOUNT_ROLE;
GRANT USAGE ON SCHEMA RAW_DEV.RAW TO ROLE DBT_LEATHER_SHOP_DEV_SERVICE_ACCOUNT_ROLE;
GRANT SELECT ON ALL TABLES IN SCHEMA RAW_DEV.RAW TO ROLE DBT_LEATHER_SHOP_DEV_SERVICE_ACCOUNT_ROLE;
-- FUTURE DATA ACCESS
GRANT CREATE SCHEMA ON DATABASE LEATHER_SHOP_DEV TO ROLE DBT_LEATHER_SHOP_DEV_SERVICE_ACCOUNT_ROLE;
GRANT CREATE TABLE, CREATE VIEW ON FUTURE SCHEMAS IN DATABASE LEATHER_SHOP_DEV TO ROLE DBT_LEATHER_SHOP_DEV_SERVICE_ACCOUNT_ROLE;

-----------------------------
