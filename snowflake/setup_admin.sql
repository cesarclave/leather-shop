---- GRANT ROLES TO USERS
USE ROLE USERADMIN;
-- SERVICE ACCOUNT USERS
GRANT ROLE DBT_LEATHER_SHOP_DEV_SERVICE_ACCOUNT_ROLE TO USER DBT_LEATHER_SHOP_DEV_SERVICE_ACCOUNT_USER;
GRANT ROLE DBT_LEATHER_SHOP_PRD_SERVICE_ACCOUNT_ROLE TO USER DBT_LEATHER_SHOP_PRD_SERVICE_ACCOUNT_USER;
-- HUMAN USERS
GRANT ROLE DATA_ANALYST_ROLE TO USER DATA_ANALYST_USER1;
