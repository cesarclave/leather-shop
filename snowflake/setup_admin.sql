GRANT USAGE ON WAREHOUSE DBT_LEATHER_SHOP_PRD_WH TO ROLE DBT_LEATHER_SHOP_PRD_SERVICE_ACCOUNT_ROLE;
-- RAW DATA ACCESS
GRANT USAGE ON DATABASE LEATHER_SHOP_PRD TO ROLE DBT_LEATHER_SHOP_PRD_SERVICE_ACCOUNT_ROLE;
GRANT USAGE ON SCHEMA RAW_PRD.RAW TO ROLE DBT_LEATHER_SHOP_PRD_SERVICE_ACCOUNT_ROLE;
GRANT SELECT ON ALL TABLES IN SCHEMA RAW_PRD.RAW TO ROLE DBT_LEATHER_SHOP_DEV_SERVICE_ACCOUNT_ROLE;
-- FUTURE DATA ACCESS
GRANT CREATE SCHEMA ON DATABASE LEATHER_SHOP_DEV TO ROLE DBT_LEATHER_SHOP_PRD_SERVICE_ACCOUNT_ROLE;
GRANT CREATE TABLE, CREATE VIEW ON FUTURE SCHEMAS IN DATABASE LEATHER_SHOP_PRD TO ROLE DBT_LEATHER_SHOP_DEV_SERVICE_ACCOUNT_ROLE;


